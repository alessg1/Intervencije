package com.intervencije.com.intervencije.greenList;


public class GreenListItems {
    String name;
    String surname;
    String timeOfcall;
    String specialThings;
    String driverLicence;
    String other;
    boolean personYellow;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getTimeOfcall() {
        return timeOfcall;
    }

    public void setTimeOfcall(String timeOfcall) {
        this.timeOfcall = timeOfcall;
    }

    public String getSpecialThings() {
        return specialThings;
    }

    public void setSpecialThings(String specialThings) {
        this.specialThings = specialThings;
    }

    public String getDriverLicence() {
        return driverLicence;
    }

    public void setDriverLicence(String driverLicence) {
        this.driverLicence = driverLicence;
    }

    public String getOther() {
        return other;
    }

    public void setOther(String other) {
        this.other = other;
    }

    public void setPersonYellow(boolean personYellow) {
        this.personYellow = personYellow;
    }

    public boolean getPersonYellow() {
        return personYellow;
    }


}

